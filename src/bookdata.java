    private void fetch() {
        try {
            Class.forName("com.mysql.jdbc.Driver");
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/library", "root", "");
            String sql = "select * from books";
            pst = conn.prepareStatement(sql);
            rs = pst.executeQuery();
            t1.setModel(DbUtils.resultSetToTableModel(rs));
            String sqll = "select SNO,BOOK_ID,BOOK_NAME, STD_ID,STD_NAME,DEGREE_COURSE,YEAR_SEMESTER,DATE_OF_ISSUE from issuedbooks";
            pstt = conn.prepareStatement(sqll);
            rss = pstt.executeQuery();
            t2.setModel(DbUtils.resultSetToTableModel(rss));
            String sqlll = "select  * from returnedbooks";
            psttt = conn.prepareStatement(sqlll);
            rsss = psttt.executeQuery();
            t3.setModel(DbUtils.resultSetToTableModel(rsss));

        } catch (ClassNotFoundException | SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }


